## The two functions below calculate and cache the inverse of the matrix provided by the user.

## The first function creates a matrix object 'x' and caches the inverse:
makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) m <<- inverse
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## The second function computes the inverse of the matrix 'x'.
## If the inverse has already been calculated (and the matrix has not changed),
## the function retrieves the inverse from the cache:
cacheSolve <- function(x, ...) {
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}